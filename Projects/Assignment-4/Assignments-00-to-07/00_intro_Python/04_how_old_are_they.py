def main():
    Anton = 21
    Beth = Anton + 6
    Chain = Beth + 20
    Drew = Chain + Anton

    print(f"Anton is {Anton}")
    print(f"Beth is {Beth}")
    print(f"Chain is {Chain}")
    print(f"Drew is {Drew}")

if __name__ == "__main__":
    main()